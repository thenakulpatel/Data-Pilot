export function logout() {

  // ================================================
  // CLEAR STORAGE
  // ================================================

  localStorage.removeItem(
    "token"
  );

  // ================================================
  // CLEAR COOKIE
  // ================================================

  document.cookie =
    "token=; Max-Age=0; path=/";

  // ================================================
  // HARD REDIRECT
  // ================================================

  window.location.replace(
    "/login"
  );
}